import Foundation

enum CalcKey: String {
    case clear      = "C"
    case decimal    = "."
    case divide     = "÷"
    case multiply   = "×"
    case subtract   = "−"
    case plusMinus  = "±"
    case percent    = "%"
    case add        = "+"
    case equals     = "="
    
    case one   = "1", two   = "2", three = "3"
    case four  = "4", five  = "5", six   = "6"
    case seven = "7", eight = "8", nine  = "9"
    case zero  = "0"
}
